package com.example.myhealthmate;

public class StressData {

    String s_date, s_nervousness, s_control, s_worry, s_relaxation, s_restlessness, s_irritability, s_fear, s_dcode, s_result;

    public StressData() {
    }

    public StressData(String s_date, String s_nervousness, String s_control, String s_worry, String s_relaxation, String s_restlessness, String s_irritability, String s_fear, String s_dcode, String s_result) {
        this.s_date = s_date;
        this.s_nervousness = s_nervousness;
        this.s_control = s_control;
        this.s_worry = s_worry;
        this.s_relaxation = s_relaxation;
        this.s_restlessness = s_restlessness;
        this.s_irritability = s_irritability;
        this.s_fear = s_fear;
        this.s_dcode = s_dcode;
        this.s_result = s_result;
    }





    public String getS_nervousness() {
        return s_nervousness;
    }

    public void setS_nervousness(String s_nervousness) {
        this.s_nervousness = s_nervousness;
    }

    public String getS_control() {
        return s_control;
    }

    public void setS_control(String s_control) {
        this.s_control = s_control;
    }

    public String getS_worry() {
        return s_worry;
    }

    public void setS_worry(String s_worry) {
        this.s_worry = s_worry;
    }

    public String getS_relaxation() {
        return s_relaxation;
    }

    public void setS_relaxation(String s_relaxation) {
        this.s_relaxation = s_relaxation;
    }

    public String getS_restlessness() {
        return s_restlessness;
    }

    public void setS_restlessness(String s_restlessness) {
        this.s_restlessness = s_restlessness;
    }

    public String getS_irritability() {
        return s_irritability;
    }

    public void setS_irritability(String s_irritability) {
        this.s_irritability = s_irritability;
    }

    public String getS_fear() {
        return s_fear;
    }

    public void setS_fear(String s_fear) {
        this.s_fear = s_fear;
    }

    public String getS_dcode() {
        return s_dcode;
    }

    public void setS_dcode(String s_dcode) {
        this.s_dcode = s_dcode;
    }

    public String getS_result() {
        return s_result;
    }

    public void setS_result(String s_result) {
        this.s_result = s_result;
    }

    public String getS_date() {
        return s_date;
    }

    public void setS_date(String s_date) {
        this.s_date = s_date;
    }
}
